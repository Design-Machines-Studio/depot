# --------------------------------------------------------------------------
# Check that agent references in dm-review SKILL.md resolve
# --------------------------------------------------------------------------
check_agent_references() {
  local skill_file="$PLUGINS_DIR/dm-review/skills/review/SKILL.md"
  local any_failed=0

  if [ ! -f "$skill_file" ]; then
    printf "  ${YELLOW}WARN${RESET}  dm-review SKILL.md not found -- skipping agent reference check\n"
    return 0
  fi

  # Extract agent file paths from the SKILL.md agent selection table
  local agent_paths
  agent_paths=$(grep -oE 'plugins/[a-z-]+/agents/(review|workflow)/[a-z0-9-]+\.md' "$skill_file" | sort -u)

  while IFS= read -r agent_path; do
    [ -z "$agent_path" ] && continue
    local full_path="$REPO_ROOT/$agent_path"
    if [ -f "$full_path" ]; then
      printf "  ${GREEN}OK${RESET}    %s\n" "$agent_path"
    else
      printf "  ${RED}FAIL${RESET}  %s (file not found)\n" "$agent_path"
      any_failed=1
    fi
  done <<< "$agent_paths"

  return $any_failed
}

# --------------------------------------------------------------------------
# Check that companion skill references in commands resolve
# --------------------------------------------------------------------------
check_companion_skills() {
  local any_failed=0

  # Find all command files with companion skill references
  local files_with_refs
  files_with_refs=$(grep -rl 'from.*plugin\|companion skill' "$PLUGINS_DIR"/*/commands/*.md 2>/dev/null || true)

  if [ -z "$files_with_refs" ]; then
    printf "  ${GREEN}OK${RESET}    No companion skill references found in commands\n"
    return 0
  fi

  # Check each command file that references companion plugins
  while IFS= read -r cmd_file; do
    [ -z "$cmd_file" ] && continue
    local cmd_name
    cmd_name=$(basename "$cmd_file" .md)

    local referenced_plugins
    referenced_plugins=$(grep -oE 'from [a-z-]+ plugin' "$cmd_file" | sed 's/from //;s/ plugin//' | sort -u)

    while IFS= read -r plugin_name; do
      [ -z "$plugin_name" ] && continue
      if [ -d "$PLUGINS_DIR/$plugin_name" ]; then
        printf "  ${GREEN}OK${RESET}    %s companion: %s (installed)\n" "$cmd_name" "$plugin_name"
      else
        printf "  ${RED}FAIL${RESET}  %s companion: %s (not installed)\n" "$cmd_name" "$plugin_name"
        any_failed=1
      fi
    done <<< "$referenced_plugins"
  done <<< "$files_with_refs"

  return $any_failed
}



# --------------------------------------------------------------------------
# Check marketplace.json versions match plugin.json versions
# --------------------------------------------------------------------------
check_marketplace_versions() {
  local marketplace_file="$REPO_ROOT/.claude-plugin/marketplace.json"
  local any_failed=0

  if [ ! -f "$marketplace_file" ]; then
    printf "  ${YELLOW}WARN${RESET}  .claude-plugin/marketplace.json not found -- skipping version sync check\n"
    return 0
  fi

  local result
  result=$(PLUGINS_DIR="$PLUGINS_DIR" MARKETPLACE="$marketplace_file" python3 << 'PYEOF'
import json, os

plugins_dir = os.environ["PLUGINS_DIR"]
marketplace_path = os.environ["MARKETPLACE"]

with open(marketplace_path) as f:
    marketplace = json.load(f)

mismatches = []
ok_count = 0

for entry in marketplace.get("plugins", []):
    name = entry["name"]
    market_ver = entry.get("version", "NONE")
    plugin_json = os.path.join(plugins_dir, name, ".claude-plugin", "plugin.json")

    if not os.path.exists(plugin_json):
        mismatches.append((name, market_ver, "NOT FOUND"))
        continue

    with open(plugin_json) as f:
        actual_ver = json.load(f).get("version", "NONE")

    if market_ver != actual_ver:
        mismatches.append((name, market_ver, actual_ver))
    else:
        ok_count += 1

# Output: first line is ok count, then mismatches
print(f"OK:{ok_count}")
for name, market_ver, actual_ver in mismatches:
    print(f"MISMATCH:{name}:{market_ver}:{actual_ver}")
PYEOF
)

  local ok_count=0
  while IFS= read -r line; do
    case "$line" in
      OK:*)
        ok_count="${line#OK:}"
        ;;
      MISMATCH:*)
        local details="${line#MISMATCH:}"
        local plugin_name market_ver actual_ver
        IFS=: read -r plugin_name market_ver actual_ver <<< "$details"
        printf "  ${RED}FAIL${RESET}  %s: marketplace.json=%s but plugin.json=%s\n" "$plugin_name" "$market_ver" "$actual_ver"
        any_failed=1
        ;;
    esac
  done <<< "$result"

  if [ "$any_failed" -eq 0 ]; then
    printf "  ${GREEN}OK${RESET}    All %s plugin versions in sync (marketplace.json == plugin.json)\n" "$ok_count"
  else
    printf "  ${YELLOW}FIX${RESET}   Update the version field in .claude-plugin/marketplace.json to match\n"
  fi

  return $any_failed
}

# --------------------------------------------------------------------------
# Check search index freshness
# --------------------------------------------------------------------------
check_index_freshness() {
  local index_file="$REPO_ROOT/docs/search-index.md"

  if [ ! -f "$index_file" ]; then
    printf "  ${YELLOW}WARN${RESET}  docs/search-index.md not found -- run with --generate-index\n"
    return 0
  fi

  # Count rows in each table section
  # Use python3 for reliable section parsing
  local counts
  counts=$(INDEX_FILE="$index_file" python3 << 'PYEOF'
import os, re
with open(os.environ["INDEX_FILE"]) as f:
    content = f.read()
sections = re.split(r'^## ', content, flags=re.MULTILINE)
skills = agents = commands = 0
for s in sections:
    rows = [l for l in s.split('\n') if l.startswith('|') and not l.startswith('|--') and not l.startswith('| Plugin') and not l.startswith('| Need')]
    if s.startswith('Skills Index'):
        skills = len(rows)
    elif s.startswith('Agents Index'):
        agents = len(rows)
    elif s.startswith('Commands Index'):
        commands = len(rows)
print(f"{skills} {agents} {commands}")
PYEOF
)
  local index_skills index_agents index_commands
  read -r index_skills index_agents index_commands <<< "$counts"

  # Count actual capabilities (single python3 call)
  local actual_counts
  actual_counts=$(PLUGINS_DIR="$PLUGINS_DIR" python3 -c "
import json, glob, os
pjs = [json.load(open(p)).get('capabilities',{}) for p in glob.glob(os.path.join(os.environ['PLUGINS_DIR'],'*/.claude-plugin/plugin.json'))]
print(sum(len(c.get('skills',[])) for c in pjs), sum(len(c.get('agents',[])) for c in pjs), sum(len(c.get('commands',[])) for c in pjs))")
  local actual_skills actual_agents actual_commands
  read -r actual_skills actual_agents actual_commands <<< "$actual_counts"

  local stale=0
  if [ "$index_skills" -ne "$actual_skills" ]; then
    printf "  ${YELLOW}WARN${RESET}  Skills: index has %s, actual is %s\n" "$index_skills" "$actual_skills"
    stale=1
  fi
  if [ "$index_agents" -ne "$actual_agents" ]; then
    printf "  ${YELLOW}WARN${RESET}  Agents: index has %s, actual is %s\n" "$index_agents" "$actual_agents"
    stale=1
  fi
  if [ "$index_commands" -ne "$actual_commands" ]; then
    printf "  ${YELLOW}WARN${RESET}  Commands: index has %s, actual is %s\n" "$index_commands" "$actual_commands"
    stale=1
  fi

  if [ "$stale" -eq 1 ]; then
    printf "  ${YELLOW}WARN${RESET}  Search index is stale -- regenerate with: ./tools/validate-composition.sh --generate-index\n"
  else
    printf "  ${GREEN}OK${RESET}    Search index is current (%s skills, %s agents, %s commands)\n" "$actual_skills" "$actual_agents" "$actual_commands"
  fi

  return 0
}

