  if ! "$SCRIPT_DIR/validate-routing-economics.sh"; then
    any_failed=1
  fi

  printf "\n${BOLD}Workflow contracts:${RESET}\n"
  if ! "$SCRIPT_DIR/validate-workflow-contracts.sh"; then
    any_failed=1
  fi

  printf "\n${BOLD}SKILL.md frontmatter integrity:${RESET}\n"
  if ! check_skill_frontmatter; then
    any_failed=1
  fi

  printf "\n${BOLD}Pipeline HTML templates:${RESET}\n"
  if ! validate_html_templates; then
    any_failed=1
  fi

  printf "\n${BOLD}Search index freshness:${RESET}\n"
  check_index_freshness

  printf "\n"
  if [ "$any_failed" -eq 1 ]; then
    printf "${RED}FAIL: Broken composition references found${RESET}\n\n"
    return 1
  else
    printf "${GREEN}PASS: All composition references valid${RESET}\n\n"
    return 0
  fi
}

